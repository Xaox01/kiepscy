  function getFavorites() {
const favorites = localStorage.getItem('favorites');
console.log('Favorites from localStorage:', favorites);
return favorites ? JSON.parse(favorites) : [];
}

  function updateFavorites() {
const favorites = getFavorites();
const favoritesList = document.getElementById('favorites');
favoritesList.innerHTML = favorites.map(episode => `
    <li>
        <a href="${episode.link}">${episode.title}</a>
        <button onclick="toggleFavorite(${JSON.stringify(episode).replace(/"/g, '&quot;')})">❌</button>
    </li>
`).join('');
}

  function toggleFavorite(episode) {
const favorites = getFavorites();
const favoriteIndex = favorites.findIndex(favorite => favorite.number === episode.number && favorite.title === episode.title);

if (favoriteIndex === -1) {
    // Odcinek nie jest w ulubionych, więc go dodaj
    favorites.push(episode);
} else {
    // Odcinek jest już w ulubionych, więc go usuń
    favorites.splice(favoriteIndex, 1);
}

localStorage.setItem('favorites', JSON.stringify(favorites));
updateFavorites();
}

 function getRandomEpisode() {
const selectedSeason = document.getElementById('season').value;
let episodesToChooseFrom;

if (selectedSeason === 'all') {
    episodesToChooseFrom = episodes;
} else {
    const selectedSeasonNumber = parseInt(selectedSeason, 10);
    episodesToChooseFrom = episodes.filter(episode => episode.season === selectedSeasonNumber);
}

if (episodesToChooseFrom.length === 0) {
    alert('Nie ma odcinków w tym sezonie.');
    return;
}

const randomIndex = Math.floor(Math.random() * episodesToChooseFrom.length);
const episode = episodesToChooseFrom[randomIndex];

document.getElementById('episode').innerHTML = `
    <p>
        ${episode.number}. ${episode.title} 
        <a href="${episode.link}" class="text-indigo-500 hover:text-indigo-700">Oglądaj</a>
        <span class="cursor-pointer" onclick="toggleFavorite(${JSON.stringify(episode).replace(/"/g, '&quot;')})">❤️</span>
    </p>
`;
}

